package javacoreproject;

public class DatatypesDemo {

	public static void main(String[] args) {
		
		
		//Integer Data Types
		
		byte bytevalue = 127;
		short shortvalue = 32767;
		int intvalue = 1234587;
		long longvalue = 99999985566655555L;
		
		//Float Data Types
		
		float floatvalue = 3.14f;
		double doublevalue = 3.1415926587;
		
		//Boolean data type
		
		boolean booleanvalueTrue = true;
		boolean booleanvalueFalse = false;
		
		//Character data type
		
		char charvalue = 'A';
		
		//print all the values
		
		System.out.println("Byte value  :" + bytevalue);
		System.out.println("short value :" +shortvalue);
		System.out.println("Int value : " +intvalue);
		System.out.println("Long value : " +longvalue);
		System.out.println("Float value : " +floatvalue);
		System.out.println("Double value : " +doublevalue);
		System.out.println("boolean value : " +booleanvalueTrue);
		System.out.println("boolean value : " +booleanvalueFalse);
		System.out.println("char value : " +charvalue);
		
		
		
		

	}

}
